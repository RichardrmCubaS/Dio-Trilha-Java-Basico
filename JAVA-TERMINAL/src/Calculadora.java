public class Calculadora {
	/**
 * Este método é utilizado para somar dois números inteiro
 * @param numeroUm este é o primeiro parâmetro do metodo
 * @param numeroDois este é o segudno parãmetro do metodo
 * @return int o resultado desete método é a soma dos dois números.
 */
  public int somar(int numeroUm, int numeroDois)  {
	  return numeroUm + numeroDois;
  }
}
